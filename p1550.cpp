#include<iostream>
using namespace std;
int main()
{
    int t;
	char a[6],b[6];
	cin>>t;
	while(t--)
	{

	cin>>a>>b;

	int c[2],d[2];
	c[0]=(a[0]-'0')*10+(a[1]-'0');
	c[1]=(a[3]-'0')*10+(a[4]-'0');
	d[0]=(b[0]-'0')*10+(b[1]-'0');
	d[1]=(b[3]-'0')*10+(b[4]-'0');


	c[1]=c[1]+d[1];


	if(c[1]>=60)
	{
	c[1]=c[1]%60;
	c[0]=c[0]+1;
	}
	c[0]=c[0]+d[0];
	c[0]=c[0]%24;


	if(c[0]<10&&c[1]<10)
	cout<<0<<c[0]<<':'<<0<<c[1]<<endl;
	if(c[0]>=10&&c[1]<10)
		cout<<c[0]<<':'<<0<<c[1]<<endl;
	if(c[0]<10&&c[1]>=10)
		cout<<0<<c[0]<<':'<<c[1]<<endl;
	if(c[0]>=10&&c[1]>>10)
		cout<<c[0]<<':'<<c[1]<<endl;





	}
return 0;
}
